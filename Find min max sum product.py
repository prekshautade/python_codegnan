#find min value
numbers = [10, 4, 2, 8, 6, -3, 5]
min_value = numbers[0]
for num in numbers:
    if num < min_value:
        min_value = num
print("Minimum element:", min_value)

#find max value
numbers = [10, 4, 2, 8, 6, -3, 5]
max_value = numbers[0]
for num in numbers:
    if num > max_value:
        max_value = num
print("Maximum element:", max_value)

#find sum of all elements in list
numbers = [10, 4, 2, 8, 6, -3, 5]
total = 0
for num in numbers:
    total += num  
print("Sum of all elements:", total)

#find product of all elements
numbers = [2, 3, 4, 5]
product = 1
for num in numbers:
    product *= num
print("Product of all elements:", product)






    
    
        

